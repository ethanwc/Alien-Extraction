class BitList {
    constructor() {
        this.copperBits = 0;
        this.crystalBits = 0;
        this.dirtBits = 0;
        this.goldBits = 0;
        this.happyBits = 0;
        this.silverBits = 0;
        this.stoneBits = 0;
    }
    reset() {
        this.copperBits = 0;
        this.crystalBits = 0;
        this.dirtBits = 0;
        this.goldBits = 0;
        this.happyBits = 0;
        this.silverBits = 0;
        this.stoneBits = 0;
    }
}