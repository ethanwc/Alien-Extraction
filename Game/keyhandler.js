document.onkeydown = function(e) {
    switch (e.keyCode) {
        case 32:
            //SPACE
            break;
        case 65:
            //LEFT
            break;
        case 87:
            //UP
            break;
        case 68:
            //RIGHT
            break;
        case 83:
            //DOWN
            break;
    }
};
document.onkeyup = function(e) {
    switch (e.keyCode) {
        case 65:
            //LEFT
            break;
        case 87:
            //UP
            break;
        case 68:
            //RIGHT
            break;
        case 83:
            //DOWN
            break;
    }
};